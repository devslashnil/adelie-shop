package com.devslashnil.adelie.util;

import com.devslashnil.adelie.model.Customer;
import org.json.JSONObject;

public class CustomerInfo {

    public JSONObject getCustomerInfo(Customer customer) {
        JSONObject customerInfo = new JSONObject();
        customerInfo.put("customerId", customer.getId());
        customerInfo.put("name", customer.getFirstName() + " " + customer.getLastName());
        customerInfo.put("email", customer.getEmail());
        customerInfo.put("address", customer.getAddress());

        return customerInfo;
    }
}
